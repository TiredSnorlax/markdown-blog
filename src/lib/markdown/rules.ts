export const rules = [
  // header rules
  [/^#{6}\s?([^\n]+)/g, '<h6>$1</h6>'],
  [/^#{5}\s?([^\n]+)/g, '<h5>$1</h5>'],
  [/^#{4}\s?([^\n]+)/g, '<h4>$1</h4>'],
  [/^#{3}\s?([^\n]+)/g, '<h3>$1</h3>'],
  [/^#{2}\s?([^\n]+)/g, '<h2>$1</h2>'],
  [/^#{1}\s?([^\n]+)/g, '<h1>$1</h1>'],

  // bold, italics and paragragh rules
  [/[\*\_]{2}([^\*\_]+)[\*\_]{2}/g, '<b>$1</b>'],
  [/[\*\_]{1}([^\*\_]+)[\*\_]{1}/g, '<i>$1</i>'],

  // links
  [/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2" style="color:#2A5DB0;text-decoration: none;">$1</a>'],

  // pre
  [/^\s*\n\`\`\`(([^\s]+))?/gm, '<pre class="$2">'],
  [/^\`\`\`\s*\n/gm, '</pre>\n\n'],

  // code
  [/[`]{1}([^`]+)[`]{1}/g, '<code>$1</code>'],

  //Image
  [/!\[([^\]]+)\]\(([^)]+)\s"([^")]+)"\)/g, '<img src="$2" alt="$1" title="$3" />']
];
