export const rules = [
	// header rules
	[/^#{6}\s?([^\n]+)/g, '<h6>$1</h6>'],
	[/^#{5}\s?([^\n]+)/g, '<h5>$1</h5>'],
	[/^#{4}\s?([^\n]+)/g, '<h4>$1</h4>'],
	[/^#{3}\s?([^\n]+)/g, '<h3>$1</h3>'],
	[/^#{2}\s?([^\n]+)/g, '<h2>$1</h2>'],
	[/^#{1}\s?([^\n]+)/g, '<h1>$1</h1>'],

	// bold, italics and paragragh rules
	[/[*_]{2}([^*_]+)[*_]{2}/g, '<b>$1</b>'],
	[/[*_]{1}([^*_]+)[*_]{1}/g, '<i>$1</i>'],
	[/[~]{2}([^~]+)[~]{2}/g, '<s>$1</s>'],

	// code
	[/[`]{1}([^`]+)[`]{1}/g, '<code>$1</code>'],
	//
	//Image
	[/!\[([^\]]+)\]\(([^)]+)\)/g, '<img src="$2" alt="$1" />'],

	// links
	[
		/\[([^\]]+)\]\((\S+)\s(.+)\)/g,
		'<a href="$2" title="$3" target="_blank" style="color:#2A5DB0">$1</a>'
	]
];

export const enterSpecialRules = [
	[/^(\t*\*[^\S\t\n\r].+)/gm, 'UNORDERED_LIST'],
	[/^\t*((?:[0-9]|[1-9][0-9]|[1-9][0-9][0-9]))\.[^\S\t\n\r].*/gm, 'ORDERED_LIST'],
	[/^`{3}(.+)?/gm, 'PRE']
];
